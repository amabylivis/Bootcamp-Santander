public class operador01 {
    public static void main(String[] args) {
        String nomeCompleto = "Linguagem" + "java";
        System.out.println(nomeCompleto);

        int numero = 5;
        numero = - numero;
        System.out.println(numero);
        numero = numero * -1;
        System.out.println(numero);

        System.out.println(++ numero);
    }
}
